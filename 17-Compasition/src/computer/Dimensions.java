package computer;

public class Dimensions {
	//1st write properties, 2nd create constructor, 3rd create getters(maybe setters)
	
	private int width;
	private int height;
	private int depth;
	
	public Dimensions(int width, int height, int depth) {
		super();
		this.width = width;
		this.height = height;
		this.depth = depth;
	}

	public int getWidth() {
		return width;
	}

	public int getHeight() {
		return height;
	}

	public int getDepth() {
		return depth;
	}
	
	
	
	
}
