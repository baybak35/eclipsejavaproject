package computer;

public class Resolution { //we create this for Resolution resolution1

	private int width;
	private int height;
	
	public Resolution(int width, int height) {
		super();
		this.width = width;
		this.height = height;
	}

	public int getWidth() {
		return width;
	}

	public int getHeight() {
		return height;
	}
	
	
	
	
}
